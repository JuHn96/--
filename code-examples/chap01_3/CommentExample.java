package chap01_3;

public class CommentExample {
    public static void main(String[] args) {
        // 한 줄 주석: 실행되지 않습니다.
        System.out.println("한 줄 주석은 // 로 시작합니다.");

        /*
         여러 줄 주석:
         이 부분도 실행되지 않습니다.
        */
        System.out.println("여러 줄 주석은 /* ... */ 로 감쌉니다.");
    }
}
